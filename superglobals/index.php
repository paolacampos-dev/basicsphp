PHP Superglobals -SG
Group of global variables that are accessible from anywhere in a PHP script, including functions and classes.
They are associative arrays that store information about things such as the current request, the server, sessions, cookies and more.
They make PHP very practical; however, if you are not careful, they can also be a source of security vulnerabilities.
Common PHP Superglobals
    $_GET;       Data from URL parameters   (variables passed to the script via query params and GET forms)
    $_POST;      Data  sent through forms
    $_SERVER;    Server and request information
    $_SESSION;   Session data 
    $_COOKIE;    Variables passed to script via HTTP cookies
    $_FILES;     Items uploaded to current scripts-files
    $_ENV;       Environment variables
    $GLOBALS;    Access global scope variables
